// Obtenir tous les pays
export const getAllCountries = () => {
  return Object.values(COUNTRIES)
}

// Obtenir un pays par code
export const getCountryByCode = (code) => {
  return COUNTRIES[code.toUpperCase()]
}

// Obtenir les pays par région
export const getCountriesByRegion = (region) => {
  return Object.values(COUNTRIES).filter(country => country.region === region)
}

// Obtenir les pays par sous-région
export const getCountriesBySubregion = (subregion) => {
  return Object.values(COUNTRIES).filter(country => country.subregion === subregion)
}

// Obtenir les pays par devise
export const getCountriesByCurrency = (currency) => {
  return Object.values(COUNTRIES).filter(country => country.currency === currency)
}

// Obtenir les pays par langue
export const getCountriesByLanguage = (language) => {
  return Object.values(COUNTRIES).filter(country => country.language === language)
}

// Rechercher des pays
export const searchCountries = (query) => {
  const searchTerm = query.toLowerCase()
  return Object.values(COUNTRIES).filter(country => 
    country.name.toLowerCase().includes(searchTerm) ||
    country.nativeName.toLowerCase().includes(searchTerm) ||
    country.code.toLowerCase().includes(searchTerm)
  )
}

// Obtenir les pays européens
export const getEuropeanCountries = () => {
  return getCountriesByRegion('Europe')
}

